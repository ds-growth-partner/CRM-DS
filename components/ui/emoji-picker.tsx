interface EmojiPickerProps {
  onSelect: (emoji: string) => void
}

export function EmojiPicker({ onSelect }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState(0)

  return (
    <div className="w-72 bg-popover border border-border rounded-xl shadow-lg overflow-hidden">
      {/* Category tabs */}
      <div className="flex border-b border-border overflow-x-auto scrollbar-none">
        {EMOJI_CATEGORIES.map((cat, i) => (
          <button
            key={cat.label}
            onClick={() => setActiveCategory(i)}
            className={`px-3 py-2 text-xs whitespace-nowrap transition-colors ${
              i === activeCategory
                ? 'text-primary border-b-2 border-primary'
                : 'text-muted-foreground hover:text-foreground'
            }`}
          >
            {cat.label}
          </button>
        ))}
      </div>

      {/* Emoji grid */}
      <div className="p-2 h-44 overflow-y-auto">
        <div className="grid grid-cols-8 gap-0.5">
          {EMOJI_CATEGORIES[activeCategory].emojis.map(emoji => (
            <button
              key={emoji}
              onClick={() => onSelect(emoji)}
              className="flex items-center justify-center h-8 w-8 text-lg rounded hover:bg-muted transition-colors"
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  )
}
